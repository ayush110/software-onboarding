def add_one(val):
    """
    Add one to a number.

    Parameters
    ----------
    val: number
        The number to which one should be added.

    Returns
    -------
    The number plus one.
    """
    return val + 1

def add_two(val):
    """
    Add 2 to a number.

    Parameters
    ----------
    val: number
        The number to which two should be added.

    Returns
    -------
    The number plus two.
    """
    return val + 2

def multiply_by_two(val):
    """
    Multiply a number by two.

    Paramters
    ---------
    val: number
        The number which is being double.

    Returns
    -------
    The number times two.
    """
    return val * 2

